/*
*********************************************************************************************************
*                                            TASK PRIORITIES
*               0=最高 31=最低。内核占用：Tick=10, Tmr=22, Stat=23, Idle=31
*               应用任务在 24~29 区间，避免与内核任务冲突
*********************************************************************************************************
*/

