#define  APP_TASK_MONITOR_PRIO                      18      /* 实时监控响应任务                          */


/*
*********************************************************************************************************
*                                            TASK STACK SIZES
*               单位 CPU_STK（4 字节）。272 = 1088 字节
*********************************************************************************************************
*/

#define  APP_TASK_START_STK_SIZE                    272     /* 起始任务栈                                */
#define  APP_TASK_BLINK_STK_SIZE                    128     /* 闪灯测试任务栈 512B                        */

